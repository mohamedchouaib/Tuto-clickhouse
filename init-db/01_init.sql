-- -----------------------------------------------
-- 1. Base de données
-- -----------------------------------------------
CREATE DATABASE IF NOT EXISTS tp_clickhouse;

-- -----------------------------------------------
-- 2. Table de logs web (MergeTree classique)
-- -----------------------------------------------
CREATE TABLE IF NOT EXISTS tp_clickhouse.web_logs
(
    timestamp     DateTime,
    user_id       UInt32,
    session_id    String,
    url           String,
    method        Enum8('GET' = 1, 'POST' = 2, 'PUT' = 3, 'DELETE' = 4),
    status_code   UInt16,
    response_time_ms UInt32,
    bytes_sent    UInt32,
    country       LowCardinality(String),
    browser       LowCardinality(String),
    os            LowCardinality(String),
    is_mobile     UInt8
)
ENGINE = MergeTree()
PARTITION BY toYYYYMM(timestamp)
ORDER BY (timestamp, user_id)
TTL timestamp + INTERVAL 1 YEAR
SETTINGS index_granularity = 8192;

-- -----------------------------------------------
-- 3. Table de métriques IoT (MergeTree)
-- -----------------------------------------------
CREATE TABLE IF NOT EXISTS tp_clickhouse.iot_metrics
(
    timestamp     DateTime,
    device_id     String,
    device_type   LowCardinality(String),
    location      LowCardinality(String),
    temperature   Float32,
    humidity      Float32,
    battery_pct   UInt8,
    signal_strength Int16
)
ENGINE = MergeTree()
PARTITION BY toYYYYMM(timestamp)
ORDER BY (device_type, device_id, timestamp)
SETTINGS index_granularity = 8192;

-- -----------------------------------------------
-- 4. Table d'événements e-commerce (MergeTree)
-- -----------------------------------------------
CREATE TABLE IF NOT EXISTS tp_clickhouse.ecommerce_events
(
    event_time    DateTime,
    event_type    Enum8('view' = 1, 'cart' = 2, 'purchase' = 3, 'refund' = 4),
    product_id    UInt32,
    category      LowCardinality(String),
    brand         LowCardinality(String),
    price         Decimal(10, 2),
    user_id       UInt32,
    user_session  String
)
ENGINE = MergeTree()
PARTITION BY toYYYYMM(event_time)
ORDER BY (event_type, category, event_time)
SETTINGS index_granularity = 8192;

